/**
 * 이름, 나이 입력하면 합쳐서 문자로 뱉어주는 함수
 * @param {string} name 사람이름 넣기
 * @param {number} age 사람나이 넣기
 * @returns 두 개 합쳐서 문자로 뱉어줌
 */

/**
 * @version 1.3.0
 * @see https://naver.com
 */
function hello(name, age) {
  return name + age;
}

/**
 * @readonly
 * @const {number}
 */

/**
 * @todo 할일 메모가능, 내일까지 뺄셈기능 추가
 */

/**
 * @deprecated 이제 이 함수 그만쓰고 다른거 쓰라고 경고!
 */

hello("hee", 20);

